								<h3>Introduction</h3>
								<p>For over 60 years, outstanding federal employees working in Honolulu – Pacific Federal Executive Board (HPFEB) agencies have been recognized at an annual awards ceremony.  Until 2011 this ceremony included a sit down type luncheon at a prestigious Waikiki hotel, with awardee and guest tickets purchased by parent agencies, supervisors and sometimes awardee family members.   The current model is that the awards ceremony is held at a government/military facility (currently the Hickam AFB Officer’s Club on Joint Base Pearl Harbor-Hickam), at no cost to federal employees or agencies.  The ceremony is held jointly with an EXPO, where federal benefit and other partners staff booths to educate employees on their offerings and benefits.   The ceremony is typically held on the Friday of the first full week in May, corresponding to Public Service Employee Recognition Week, nationally. </p>
