								<p>The U.S. Office of Personnel Management’s, Center for Leadership Develop in partnership with the Honolulu-Pacific Federal Executive Board is proud to offer its annual Pacific Leadership Academy (PLA) Program in Honolulu, Hawaii.  The Pacific Leadership Program began in 2003 and has graduated more than 800 mid-to-senior level Fellows.  This program is designed to meet the leadership development needs of selected federal professionals at the GS-13-15 and military equivalent levels who work in federal government organizations in the Pacific Rim. </p>
